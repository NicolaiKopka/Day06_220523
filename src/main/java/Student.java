class Student {
    private String name;
    private int id;
    private String email;

    String getName() {
        return name;
    }

    int getId() {
        return id;
    }

    String getEmail() {
        return email;
    }

    void setName(String name) {
        this.name = name;
    }

    void setId(int id) {
        this.id = id;
    }

    void setEmail(String email) {
        this.email = email;
    }
}
